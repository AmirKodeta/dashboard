module.exports.monthChanger = (month) => {
  if (month == 1) {
    month = "فروردین";
  }
  if (month == 2) {
    month = "اردیبهشت";
  }
  if (month == 3) {
    month = "خرداد";
  }
  if (month == 4) {
    month = "تیر";
  }
  if (month == 5) {
    month = "مرداد";
  }
  if (month == 6) {
    month = "شهریور";
  }
  if (month == 7) {
    month = "مهر";
  }
  if (month == 8) {
    month = "آبان";
  }
  if (month == 9) {
    month = "آذر";
  }
  if (month == 10) {
    month = "دی";
  }
  if (month == 11) {
    month = "بهمن";
  }
  if (month == 12) {
    month = "اسفند";
  }
  return month;
};
